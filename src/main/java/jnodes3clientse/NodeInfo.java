private class ArpTableModel extends AbstractTableModel {

        private final String[] columns = {"IP Address", "MAC Address", "Interface", "Type"};
        private List<snmpGetARPList.ArpEntry> entries = new ArrayList<>();

        public void setEntries(List<snmpGetARPList.ArpEntry> list) {
            if (list != null) {
                entries = new ArrayList<>(list);
            } else {
                entries = new ArrayList<>();
            }
            fireTableDataChanged();
        }

        @Override
        public int getRowCount() {
            return entries.size();
        }

        @Override
        public int getColumnCount() {
            return columns.length;
        }

        @Override
        public String getColumnName(int column) {
            return columns[column];
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            snmpGetARPList.ArpEntry e = entries.get(rowIndex);

            switch (columnIndex) {
                case 0: // IP Address
                    return e.ip;

                case 1: // MAC Address
                    return e.mac;

                case 2: // Interface
                    // Prefer ifName from SNMP, otherwise resolve from local iflist
                    if (e.ifName != null && !e.ifName.isEmpty()) {
                        return e.ifName;
                    }
                    return getIfLabelForIndex(e.ifIndex);

                case 3: // Type (human readable)
                    switch (e.type) {
                        case 1:
                            return "other";
                        case 2:
                            return "invalid";
                        case 3:
                            return "dynamic";
                        case 4:
                            return "static";
                        default:
                            return "unknown";
                    }

                default:
                    return "";
            }
        }
    }

    private class ConnectedDevicesTableModel extends AbstractTableModel {

        private final String[] columns = {"MAC Address", "Interface", "Status"};
        private List<snmpGetMACList.NeighborEntry> neighbors = new ArrayList<>();

        public void setNeighbors(List<snmpGetMACList.NeighborEntry> list) {
            if (list != null) {
                neighbors = new ArrayList<>(list);
            } else {
                neighbors = new ArrayList<>();
            }
            fireTableDataChanged();
        }

        @Override
        public int getRowCount() {
            return neighbors.size();
        }

        @Override
        public int getColumnCount() {
            return columns.length;
        }

        @Override
        public String getColumnName(int column) {
            return columns[column];
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            snmpGetMACList.NeighborEntry n = neighbors.get(rowIndex);

            switch (columnIndex) {
                case 0: // MAC Address
                    return n.mac;

                case 1: // Interface label
                    // if snmpGetMACList fills ifName, you can use that directly:
                    if (n.ifName != null && !n.ifName.isEmpty()) {
                        return n.ifName;
                    }
                    // fallback using our local NodeInterface list:
                    return getIfLabelForIndex(n.ifIndex);

                case 2: // Status, human readable
                    switch (n.fdbStatus) {
                        case 1:
                            return "other";
                        case 2:
                            return "invalid";
                        case 3:
                            return "learned";
                        case 4:
                            return "self";
                        case 5:
                            return "mgmt";
                        default:
                            return "unknown";
                    }

                default:
                    return "";
            }
        }
    }

